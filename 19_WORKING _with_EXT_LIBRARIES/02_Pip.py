# Package Management (using pip ): 
# pip is Python’s package installer.
# It’s used to install, upgrade, and manage external libraries.


# Installing a package:
'''pip install requests'''     # Installs the "requests" library
'''pip install numpy==1.20.0'''     # Installs a specific version
'''pip install moviepy'''

# During the installation of any package suppose "Tensorflow" we se the list of collected packages these are dependencies a package needs for proper and optimal performance and functioning.



# Listing installed packages:
'''pip list''' 

# Upgrading a package:
'''pip install --upgrade requests'''

# Uninstalling a Package:
'''pip uninstall requests'''

