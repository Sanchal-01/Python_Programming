a = 10
a += 5    # Equivalent to a = (a+5)
print(a)

a -= 5    # Equivalent to a = (a-5)
print(a)

a *= 5    # Equivalent to a = (a*5)
print(a)

a /= 5    # Equivalent to a = (a/5)
print(a)

a **= 5    # Equivalent to a = (a**5)
print(a)

a //= 5    # Equivalent to a = (a//5)
print(a)

a %= 5    # Equivalent to a = (a%5)
print(a)
