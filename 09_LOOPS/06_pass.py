# pass is also a loop control statement.
# The pass statement is a placeholder that does nothing. It is used while syntax requires a statement but no action currently needed.
# Used because we want to write some piece of code later, so for that moment we use pass.

#Example
 
for i in range (5):
    if i == 3:
        pass   #Do nothing just get out of the loop.
    print(i)
    print("Hey!!,My name is Sanchal")
