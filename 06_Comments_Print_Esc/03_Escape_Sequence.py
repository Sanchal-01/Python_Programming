# Escape sequences are used to include special characters in strings.
# Common escape sequences: 
'''
 \n : Newline
 \t : Tab
 \\ : Backslash
 \" : Double quote
 \' : Single quote
'''

# \n is called escape Sequence Characters and this character is used to break line.
print("Hello\nWorld!")


# \t is also a escape sequence character which is used to create indentation between strings.
print("This is a tab\tcharacter.")


# \\ is also a escape sequence character which is used to create a backslash between string values between strings.
print("What do you\\ want to say ")


# \" is also a escape sequence character which is used to create a double quote on selective strings.
print("I will have a cup of \"coffee\" ")


# \' is also a escape sequence character which is used to create a single quote on selective strings.
print("I will have a cup of \'tea\' ")
